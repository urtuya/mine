int	ft_isalpha(int c)
{
	if (c <= 255 && c >= 0)
		if (((unsigned char)c <= 'z' && (unsigned char)c >= 'a')
			|| ((unsigned char)c <= 'Z' && (unsigned char)c >= 'A'))
			return (1);
	return (0);
}
