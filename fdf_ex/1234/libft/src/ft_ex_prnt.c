#include "libft.h"

void	ft_ex_prnt(int **mat, int str, int stb)
{
	int i;

	if (!mat || !(*mat))
		return ;
	i = 0;
	while (i < str)
	{
		ft_ex_prnt_arr(mat[i], stb);
		i++;
	}
}
