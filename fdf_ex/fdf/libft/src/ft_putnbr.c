#include "libft.h"

void	ft_putnbr(int n)
{
	int i;
	int copy;

	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	copy = n;
	i = 1;
	while (copy / 10 > 0)
	{
		i = i * 10;
		copy = copy / 10;
	}
	while (i > 0)
	{
		ft_putchar(n / i % 10 + '0');
		i = i / 10;
	}
}
